public class EmotionSmb : StateMachineBehaviour {

    // OnStateEnter is called before OnStateEnter is called on any state inside this state machine
	override public void OnStateEnter(Animator animator, AnimatorStateInfo stateInfo, int layerIndex) {
        // Reset all emotion triggers when changing emotions to clear queued emotions
        foreach (NPCDialogue.Emotion emotion in System.Enum.GetValues(typeof(NPCDialogue.Emotion)))
        {
            animator.ResetTrigger(emotion.ToString());
        }
    }

